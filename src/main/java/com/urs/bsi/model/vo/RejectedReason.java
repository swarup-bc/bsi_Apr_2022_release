package com.urs.bsi.model.vo;

public class RejectedReason {

	private String documentResourceIdentifier;
	private String code;
	private String text;
	public String getDocumentResourceIdentifier() {
		return documentResourceIdentifier;
	}
	public void setDocumentResourceIdentifier(String documentResourceIdentifier) {
		this.documentResourceIdentifier = documentResourceIdentifier;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
	
}
