package com.urs.bsi.model.vo;

public class DocumentInformation {

	private String file_id;
	private String document_status;
	private String document_rejection_reason;
	private String document_notes;
	private String document_id;
	public String getFile_id() {
		return file_id;
	}
	public void setFile_id(String file_id) {
		this.file_id = file_id;
	}
	public String getDocument_status() {
		return document_status;
	}
	public void setDocument_status(String document_status) {
		this.document_status = document_status;
	}
	public String getDocument_rejection_reason() {
		return document_rejection_reason;
	}
	public void setDocument_rejection_reason(String document_rejection_reason) {
		this.document_rejection_reason = document_rejection_reason;
	}
	public String getDocument_notes() {
		return document_notes;
	}
	public void setDocument_notes(String document_notes) {
		this.document_notes = document_notes;
	}
	public String getDocument_id() {
		return document_id;
	}
	public void setDocument_id(String document_id) {
		this.document_id = document_id;
	}
	
}
